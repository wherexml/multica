export const SettingsMessages: Record<LexiconLocale, Record<SettingsMessageKey, string>> = {
  "en-US": enUSSettings,
  "zh-CN": zhCNSettings,
};

type SettingsInterpolationParams = Record<string, string>;

function interpolate(template: string, params?: SettingsInterpolationParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (match, key: string) => params[key] ?? match);
}

function isLexiconKey(key: string): key is LexiconKey {
  return key in Lexicon["zh-CN"];
}

export function getSettingsLocale(): LexiconLocale {
  return getClientLocale(DEFAULT_SETTINGS_LOCALE);
}

export function settingsT(
  key: string,
  locale?: string,
  params?: SettingsInterpolationParams,
): string {
  const normalizedLocale = normalizeLocale(locale ?? DEFAULT_SETTINGS_LOCALE);
  const message = SettingsMessages[normalizedLocale][key as SettingsMessageKey];

  if (message) {
    return interpolate(message, params);
  }

  if (isLexiconKey(key)) {
    return lexiconT(key, normalizedLocale);
  }

  return interpolate(key, params);
}
